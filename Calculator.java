///Contains different methods that perform simple arithmetic operations on two operands.
class Main {
  
  public static void addition(int number1, int number2) {
		System.out.println("The sum of " + number1 + " and " + number2 + " is " + (number1+number2) + ".");
	}

	public static void subtraction (int number1, int number2) {
		System.out.println("The diference between " + number1 + " and " + number2 + " is " + (number1-number2) + ".");
	}

	public static void multiplication (int number1, int number2) {
		System.out.println(number1 + " multiplied by " + number2 + " is " + (number1*number2) + ".");
	}

	public static void division (int number1, int number2) {
		System.out.println(number1 + " divided by " + number2 + " is " + (number1/number2) + ".");
	}

	public static void modulo (int number1, int number2) {
		System.out.println("The remainder of dividing " + number1 + " by " + number2 + " is " + (number1%number2) + ".");
	}

  public static void main(String[] args) {
    addition(5,8);
    subtraction(97, 60);
    multiplication(8,9);
    division(100, 20);
    modulo(9,4);
  }
}